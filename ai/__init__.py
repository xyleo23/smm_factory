class SEOChecker:
    """Checks content for SEO compliance."""
    
    @classmethod
    def check(cls, text: str, keywords: List[str]) -> Dict[str, Any]:
        """
        Check content for SEO compliance.
        
        Args:
            text: Content text
            keywords: SEO keywords
            
        Returns:
            SEO check results with pass/fail and issues
        """
        try:
            issues = []
            text_lower = text.lower()
            
            # Check keyword presence
            missing_keywords = []
            for keyword in keywords:
                if keyword.lower() not in text_lower:
                    missing_keywords.append(keyword)
            
            if missing_keywords:
                issues.append(f"Missing keywords: {', '.join(missing_keywords)}")
            
            # Check content length
            word_count = len(text.split())
            if word_count < 300:
                issues.append(f"Content too short ({word_count} words, minimum 300)")
            
            # Check heading presence
            if "##" not in text and "<h" not in text.lower():
                issues.append("No headings found")
            
            passed = len(issues) == 0
            
            logger.info(f"SEO check: {'PASSED' if passed else 'FAILED'} ({len(issues)} issues)")
            
            return {
                "passed": passed,
                "issues": issues,
                "word_count": word_count,
                "keyword_density": sum(text_lower.count(kw.lower()) for kw in keywords) / word_count if word_count > 0 else 0,
            }
            
        except Exception as e:
            logger.error(f"Error checking SEO: {e}")
            return {
                "passed": False,
                "issues": [f"Error during check: {e}"],
                "word_count": 0,
                "keyword_density": 0,
            }


