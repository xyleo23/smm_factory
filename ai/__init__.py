    def _extract_key_points(cls, content: str) -> List[str]:
        """Extract key points from content (placeholder)."""
        paragraphs = content.split("\n\n")
        return [p[:100] + "..." for p in paragraphs[:3] if len(p) > 50]
    
    @classmethod
    def _extract_topics(cls, title: str, content: str) -> List[str]:
        """Extract topics from content (placeholder)."""
        common_words = {"и", "в", "на", "с", "по", "для", "как", "это", "что", "the", "a", "an", "in", "on", "at", "to"}
        words = (title + " " + content).lower().split()
        word_freq = {}
        
        for word in words:
            if len(word) > 4 and word not in common_words:
                word_freq[word] = word_freq.get(word, 0) + 1
        
        sorted_words = sorted(word_freq.items(), key=lambda x: x[1], reverse=True)
        return [word for word, _ in sorted_words[:5]]


